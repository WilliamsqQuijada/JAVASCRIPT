//OPERADORES DE COMPARACION

let a = 5;
let b = "5";

console.log(a,b);

// OPERADOR DE IGUALDAD
console.log(a == b);

// OPERADOR IGUALDAD ESTRICTA
// compara valor y dato
console.log(a === b);
console.log(`La variable: ${a} tiene diferente tipo de dato que ${b}`);

// OPERADOR  DISTINTO
console.log(`${a} != ${b} -> ${a != b}`)

// OPERADOR DISTINTO EXACTO
// compara valor y dato
console.log(`${a} !== ${b} -> ${a !== b}`)

// OPERADOR MENOR QUE 
console.log( a < b)

// OPERADOR MAYOR QUE
console.log( a > b)

// OPERADOR MENOR O IGUAL QUE
console.log( a <= b)

// OPERADOR MAYOR O IGUAL QUE
console.log( a >= b)