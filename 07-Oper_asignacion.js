//OPERADORES DE ASIGNACION

let miNumero =  10;
console.log(miNumero);

miNumero = 20;
console.log(miNumero);

// OPERADOR +=

miNumero += 5;
console.log(miNumero);

// OPERADOR -=

miNumero -= 5;
console.log(miNumero);

// OPERADOR *=

miNumero *= 2;
console.log(miNumero);

// OPERADOR /=

miNumero /= 4;
console.log(miNumero)

// OPERADOR %=

miNumero %= 2;
console.log(miNumero)

// OPERADOR **=

miNumero = 5;
miNumero **= 2
console.log(miNumero)