// OPERADORES LOGICOS
let a = true;
let b = false;

console.log(a,b)
console.log(typeof a)
console.log(typeof b)

// OPERADOR && AND
// SOLO REGRESA SI AMBOS SON VERDADEROS
console.log(`${a} && ${b} -> ${a && b}`)

// OPERADOR || OR
// REGRESA VERDADERO SI UNO ES VERDADEROS
console.log(`${a} || ${b} -> ${a || b}`)

// OPERADOR ! NOT 
// INVIERTE EL VALOR ORIGINAL
console.log(`${a} -> ${!a}`)
