// EJERCICIO DEL DIA DE LA SEMANA CON ELSE IF

// DECLARAMOS VARIABLE PARA HACER REFERENCIA AL DIA DE LA SEMANA 
let dia = 6;

if (dia == 1){
    console.log("LUNES");
}
else if (dia == 2){
    console.log("MARTES");
}
else if (dia == 3){
    console.log("MIERCOLES");
}
else if (dia == 4){
    console.log("JUEVES");
}
else if (dia == 5){
    console.log("VIERNES");
}
else if (dia == 6){
    console.log("SABADO");
}
else if (dia == 7){
    console.log("DOMINGO");
}
else{
    console.log("NO EXISTE TAL DIA");
}